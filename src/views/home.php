<h1 class="tracking-in-expand-fwd slot-0" >SurfAdvisor</h1>

<p class="">
    Bonjour ami(es) surfeur(es) et bienvenue sur ce site qui a pour vocation d'améliorer notre passion commune ! <br/>
    Comme vous le savez, surfez est une pratique extraodinaire, avec ces destinations paradisiaque, une culture du cool, la pratique du sport en plaine air.<br/>
    Néanmoins comme nous le savons une triste réalité se cache derrière ce rêve.<br/><br/>

    En effet, les eaux d'en lequelles nous pratiquons notre sport deviennent de plus en plus polluées, que ce soit chimiquement ou bactériologiquement, sans oublier les déchets evidemment.<br/>
    Ils envahissent chaque jour nos océans et détruisent son écosystème à petit feu.<br/><br/>

    Pour agir contre cela, <a href="https://surfrider.eu/">Surfrider</a>, avec qui nos collaborons pour ecrire ce site, souhaite augmenter la surveillance de nos eaux et vous pouvez y contribuer !!
</p>

